package menmento;

public class memento {

	private int age;
	private String name;
	private int solory;
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSolory() {
		return solory;
	}
	public void setSolory(int solory) {
		this.solory = solory;
	}
	
	
	
}
